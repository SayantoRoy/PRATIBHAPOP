    CONSTRAINT [FK_Commitment_Currency] FOREIGN KEY ([CurrencyId]) REFERENCES [SCS].[Currency] ([Id]),
    CONSTRAINT [FK_Commitment_Entity] FOREIGN KEY ([EntityId]) REFERENCES [ORG].[Entity] ([Id]),
    CONSTRAINT [FK_Commitment_Process] FOREIGN KEY ([ProcessId]) REFERENCES [HKP].[Process] ([Id]),
    CONSTRAINT [FK_Commitment_ProductMaster] FOREIGN KEY ([ProductMasterId]) REFERENCES [MST].[ProductMaster] ([Id]),
    CONSTRAINT [FK_Commitment_Season] FOREIGN KEY ([SeasonId]) REFERENCES [HKP].[Season] ([Id]),
    CONSTRAINT [FK_Commitment_SubProcess] FOREIGN KEY ([SubProcessId]) REFERENCES [HKP].[SubProcess] ([Id])
);

